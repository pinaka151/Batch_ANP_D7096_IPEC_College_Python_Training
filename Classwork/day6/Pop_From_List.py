# Creating a list with elements
list = [1,2,3,4,5,5,42]

# Deleting the last item from the list using pop method

# case-1
list.pop()
# print the list after pop
print(list)
# output
# [1, 2, 3, 4, 5, 5]


# case-2
list.pop(3)
# print the list using pop indexing 
print(list)

# output
# [1, 2, 3, 5, 5, 42]


