# function to add two numbers

def Calculation_addition(a,b):
    return (a + b)

# function to subtract two number
def Calculation_subtraction(a,b):
    return (a - b)

# function to multiply two number
def Calculation_multiplication(a,b):
    return (a * b)

# function to divide two number
def Calculation_divition(a,b):
    if b == 0 :
        exit("Please enter valid number as b can't be 0")
    return (a / b)

# function to get remainder between two number
def Calculation_remainder(a,b):
    return (a % b)